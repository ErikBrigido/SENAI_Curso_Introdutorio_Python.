import math
raiz_quadrada = math.sqrt(25)
print(raiz_quadrada)  # Resultado: 5.0

potencia = math.pow(2, 3)
print(potencia)  # Resultado: 8.0

logaritmo = math.log(10, 2)
print(logaritmo)  # Resultado: 3.3219280948873626

exponencial = math.exp(2)
print(exponencial)  # Resultado: 7.38905609893065

angulo_radianos = math.radians(45)
seno = math.sin(angulo_radianos)
cosseno = math.cos(angulo_radianos)
tangente = math.tan(angulo_radianos)

pi = math.pi
print(pi)  # Resultado: 3.141592653589793